<div class="center"><h1>Blíží se nám jaro!</h1></div>
<b>Je nejvyšší čas myslet na zahradu. Ať už ji máte, nebo se chystáte na její založení u nového domu.</b>
<img src="img/jaro1.jpeg" class="addborder floatRight"/>
<p>Delší den, slunce a&nbsp;příjemný teplý větřík nenechá v klidu nikoho. V&nbsp;našich zahradách začíná být živo. Kdo může, vytahuje očima ze země lístky a poupata jarních cibulovin. A&nbsp;opravdu, talovíny a&nbsp;sněženky už&nbsp;jsou venku! Včely kontrolují, jestli je už&nbsp;něco k&nbsp;snědku a&nbsp;přiletěli první zpěvní ptáci.</p>
<p>A co Vy? Máte nachystané hrábě na vyhrabání trávníku? Vyčištěné ptačí budky, ostré nůžky na úklid na trvalkových plochách?<br /><b>... Plán nové zahrady?</b></p>

<p>Zahradní architekti totiž dokončují návrhy, které budou moci hned časně z jara realizovat. Kdo zaváhá, bude muset počkat. Ono udělat kvalitní návrh a odpovídající nabídku chvíli trvá. V posledních letech navíc začíná jaro rychle. Zimní boty měníme hned za&nbsp;sandály. Je to příjemné, ale nevyhovuje to dřevinám - zvláště stromům, které se sází s&nbsp;balem nebo přímo prostokořenné. V&nbsp;teple rychle raší a&nbsp;tím se zkracuje doba pro výsadbu. Zváště velké je nebezpečí u&nbsp;časně rašících druhů (např. okrasných slivoní).</p>
<p>Právě stromy tvoří "kostru" zahrady. Jistě můžeme vysadit keřové podsadby a nechat prázdnou výsadbovou jámu pro strom, ale správné je přihlédnout při výsadbě i k osobitosti jednotlivých stromů. Měly by přirozeně navazovat, je třeba něco posunout, něco pootočit a to se už v částečně založených plochách špatně dělá. Proto je dobré začít včas! Vyhnete se tak komplikacím a zbytečně vynaložené práci, pokud by se výsadba musela odložit na podzim.</p>

